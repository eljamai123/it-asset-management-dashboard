export type AssetStatus = "Active" | "Maintenance" | "Inactive" | "Retired"
export type TicketPriority = "Low" | "Medium" | "High" | "Critical"
export type TicketStatus = "Open" | "In Progress" | "Resolved"
export type UserRole = "Admin" | "Technician" | "Employee"

export interface Asset {
  id: string
  computerName: string
  serialNumber: string
  brand: string
  model: string
  cpu: string
  ram: string
  storage: string
  ipAddress: string
  assignedUser: string
  status: AssetStatus
  location: string
  purchaseDate: string
}

export interface Ticket {
  id: string
  title: string
  description: string
  priority: TicketPriority
  status: TicketStatus
  requester: string
  assignee: string
  asset: string
  category: string
  createdAt: string
  updatedAt: string
}

export interface User {
  id: string
  name: string
  email: string
  role: UserRole
  department: string
  phone: string
  location: string
  status: "Active" | "Inactive"
  assignedAssets: number
}

